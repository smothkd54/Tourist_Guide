@app.route("/nearby")
def nearby():
    try:
        lat = float(request.args.get("lat"))
        lon = float(request.args.get("lon"))
    except (TypeError, ValueError):
        return jsonify({"error": "Provide numeric lat and lon query params"}), 400
    radius_m = float(request.args.get("radius_m", 500))

    geocoded = [
        lm for lm in current_app.landmarks_by_id.values()
        if lm.get("lat") is not None and lm.get("lon") is not None
    ]

    in_range = []
    nearest_overall = None
    nearest_dist = None

    for lm in geocoded:
        dist = haversine_m(lat, lon, lm["lat"], lm["lon"])
        if nearest_dist is None or dist < nearest_dist:
            nearest_dist = dist
            nearest_overall = lm
        if dist <= radius_m:
            in_range.append({**lm, "distance_m": round(dist, 1)})

    in_range.sort(key=lambda x: x["distance_m"])

    # Always report how far the nearest landmark actually is, even when
    # nothing falls inside the requested radius. Without this, a tourist
    # in Moscow or abroad gets an empty list and no indication of why —
    # it looks identical to a bug. With it, the frontend can say "you're
    # 1,100 km away" instead of silently showing nothing.
    response = {
        "query": {"lat": lat, "lon": lon, "radius_m": radius_m},
        "count": len(in_range),
        "landmarks": in_range,
    }
    if nearest_overall is not None:
        response["nearest_landmark_distance_m"] = round(nearest_dist, 1)
        response["nearest_landmark_name"] = nearest_overall["name"]

    return jsonify(response)


@app.route("/plan", methods=["GET"])
def dynamic_plan():
    """
    Dynamic route planning via OSRM — computed live, no pre-generated file needed.

    Query params:
      start   : landmark id (required)
      minutes : int, time budget in minutes (default 60)
      visit   : int, minutes spent per stop (optional). If omitted, each
                stop's dwell time is estimated automatically from its
                landmark type (a museum gets more time than a sculpture)
                instead of every stop costing the same fixed amount.
                Pass this only to force every stop to the same duration.
    """
    start_id = request.args.get("start")
    if not start_id:
        return jsonify({"error": "Missing 'start' landmark id"}), 400

    try:
        minutes = int(request.args.get("minutes", 60))
        visit_param = request.args.get("visit")
        visit_minutes = int(visit_param) if visit_param is not None else None
    except ValueError:
        return jsonify({"error": "minutes and visit must be integers"}), 400

    geocoded = [lm for lm in current_app.landmarks_by_id.values() if lm.get("lat") is not None]
    if len(geocoded) < 2:
        return jsonify({"error": "Not enough geocoded landmarks (need at least 2)."}), 400

    if not _osrm_limiter.is_allowed(request.remote_addr):
        return jsonify({"error": "Rate limit exceeded. Try again in a minute."}), 429

    try:
        start_idx = next(i for i, lm in enumerate(geocoded) if lm["id"] == start_id)
    except StopIteration:
        return jsonify({"error": f"Landmark '{start_id}' not found or has no coordinates."}), 404

    try:
        route_data = plan_route(geocoded, start_idx, minutes, visit_minutes)
    except Exception as e:
        _metrics.record_osrm(False)
        logger.error("OSRM routing failed for start=%s minutes=%s: %s", start_id, minutes, e)
        return jsonify({"error": f"OSRM routing failed: {str(e)}"}), 503

    _metrics.record_osrm(True)
    return jsonify(route_data)


@app.route("/plan-custom", methods=["POST"])
def plan_custom():
    """
    Plan a walking route for user-selected landmarks.

    POST body:
      {
        "landmarks": ["id1", "id2", ...],  (required, list of landmark IDs)
        "start": "id1",                     (required, starting landmark ID)
        "optimize": true                    (optional, default true)
      }
    """
    body = request.get_json(silent=True)
    if not body or "landmarks" not in body or "start" not in body:
        return jsonify({"error": "Send JSON with 'landmarks' (list of IDs) and 'start' (landmark ID)"}), 400

    landmark_ids = body["landmarks"]
    start_id = body["start"]
    optimize = body.get("optimize", True)

    if not isinstance(landmark_ids, list) or len(landmark_ids) == 0:
        return jsonify({"error": "'landmarks' must be a non-empty list of landmark IDs"}), 400

    landmarks = [current_app.landmarks_by_id[lid] for lid in landmark_ids if lid in current_app.landmarks_by_id]
    if len(landmarks) == 0:
        return jsonify({"error": "None of the provided landmark IDs were found"}), 404

    geocoded = [lm for lm in landmarks if lm.get("lat") is not None]
    if len(geocoded) < 1:
        return jsonify({"error": "Selected landmarks need GPS coordinates"}), 400

    if not _osrm_limiter.is_allowed(request.remote_addr):
        return jsonify({"error": "Rate limit exceeded. Try again in a minute."}), 429

    try:
        route_data = plan_custom_route(geocoded, start_id, optimize_order=optimize)
    except Exception as e:
        _metrics.record_osrm(False)
        logger.error("OSRM routing failed for custom route: %s", e)
        return jsonify({"error": f"OSRM routing failed: {str(e)}"}), 503

    _metrics.record_osrm(True)
    return jsonify(route_data)


