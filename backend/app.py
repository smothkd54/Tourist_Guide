def _is_lfs_pointer(path: Path) -> bool:
    """Return True if the file is a Git LFS pointer stub, not the real content."""
    if path.stat().st_size > 1024:
        return False
    try:
        return path.read_text(encoding="utf-8", errors="ignore") \
                   .startswith("version https://git-lfs.github.com")
    except Exception:
        return False


def load_resources():
    """Load model, class names, and landmark metadata into app state."""
    if MODEL_PATH.exists() and not _is_lfs_pointer(MODEL_PATH):
        app.model = keras.models.load_model(MODEL_PATH)
        logger.info("Model loaded from %s", MODEL_PATH)
    elif MODEL_PATH.exists():
        logger.error(
            "Model at %s is a Git LFS pointer — run 'git lfs pull' to "
            "fetch the real file. /predict will return 503 until then.",
            MODEL_PATH,
        )
    else:
        logger.error(
            "Model not found at %s — run train.py first. "
            "/predict will return 503.",
            MODEL_PATH,
        )

    if CLASS_NAMES_PATH.exists():
        app.class_names = json.loads(CLASS_NAMES_PATH.read_text())
    else:
        logger.warning("%s not found. Run train.py first.", CLASS_NAMES_PATH)

    if LANDMARKS_PATH.exists():
        data = json.loads(LANDMARKS_PATH.read_text())
        app.landmarks_by_id = {lm["id"]: lm for lm in data["landmarks"]}
    else:
        raise FileNotFoundError(f"landmarks.json not found at {LANDMARKS_PATH}")

    logger.info("Classes loaded: %d", len(app.class_names))
    logger.info("Landmarks in DB: %d", len(app.landmarks_by_id))


def preprocess_image(image_bytes: bytes) -> np.ndarray:
    """Decode, resize, normalise to [0,1], add batch dim."""
    img = Image.open(io.BytesIO(image_bytes)).convert("RGB")
    img = img.resize(IMG_SIZE, Image.LANCZOS)
    arr = np.array(img, dtype=np.float32) / 255.0
    return np.expand_dims(arr, axis=0)


def decode_base64_image(b64_string: str) -> bytes:
    """Handle both raw base64 and data-URI prefixed strings."""
    if "," in b64_string:
        b64_string = b64_string.split(",", 1)[1]
    return base64.b64decode(b64_string)


def haversine_m(lat1, lon1, lat2, lon2):
    import math
    R = 6_371_000
    phi1, phi2 = math.radians(lat1), math.radians(lat2)
    dphi = math.radians(lat2 - lat1)
    dlambda = math.radians(lon2 - lon1)
    a = math.sin(dphi / 2) ** 2 + math.cos(phi1) * math.cos(phi2) * math.sin(dlambda / 2) ** 2
    return 2 * R * math.asin(math.sqrt(a))


